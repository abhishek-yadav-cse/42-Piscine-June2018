int		ft_str_is_lowercase(char *str)
{
	int count;
	int i;

	i = 0;
	count = 0;
	if (str[0] == '\0')
		return (1);
	while (str[i] != '\0')
	{
		if (!((str[i] >= 97 && str[i] <= 122)))
			count++;
		i++;
	}
	if (count != 0)
		return (0);
	else
		return (1);
}
