int		ft_str_is_numeric(char *str)
{
	int count;
	int i;

	i = 0;
	count = 0;
	while (str[i] != '\0')
	{
		if (!((str[i] >= 48 && str[i] <= 57)))
			count++;
		i++;
	}
	if (count != 0)
		return (0);
	else
		return (1);
}
